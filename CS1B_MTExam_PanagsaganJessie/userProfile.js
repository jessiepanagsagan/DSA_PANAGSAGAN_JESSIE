// Collect user information using prompt()

const fullName = prompt("Enter your full name (first and last name):"); //This Prompt is asking the (first and last name) of the student.
const age = prompt("Enter your age:"); //This Prompt is asking the (Age) of the student.
const favoriteNumber = prompt("Enter your favorite number:"); //This Prompt is asking the (favorite number) of the student.
const favoriteColor = prompt("Enter your favorite color:"); //This Prompt is asking the (favorite color) of the student.

console.log("User Information:"); // This consol.log are Displaying a heading to organize the output
console.log("Full Name: " + fullName); //This will concatenate and display the user's full name
console.log("Age: " + age); // This will concatenate and display the user's age
console.log("Favorite Number: " + favoriteNumber); //This will concatenate and display the user's favorite number
console.log("Favorite Color: " + favoriteColor); //This will concatenate and display the user's favorite color