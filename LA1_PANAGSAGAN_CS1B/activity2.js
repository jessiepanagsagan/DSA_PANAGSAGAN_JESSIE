//Numbers Activity

//#1. Create a variable named firstFavNum and store your favorite number. 
firstFavNum = 18

//#2. Create a variable named secondFavNum and store your second favorite number. 
secondFavNum = 7

//#3. Perform the following operations: Addition, Subtraction, Multiplication, Division, Modulus, Exponentiation
