//Comparison or Relational Operator


let firstFavNumb = 18;
let secondFavNumb = 7;

console.log("Greater than:");
console.log(firstFavNumb > secondFavNumb);

console.log("\nLess than:");
console.log(firstFavNumb < secondFavNumb);

console.log("\nGreater than or equal to:");
console.log(firstFavNumb >= secondFavNumb);

console.log("\nLess than or equal to:");
console.log(firstFavNumb <= secondFavNumb);

console.log("\nStrict equality (===):");
console.log(firstFavNumb === secondFavNumb);

console.log("\nLoose equality (==):");
console.log(firstFavNumb == secondFavNumb);

console.log("\nStrict inequality (!==):");
console.log(firstFavNumb !== secondFavNumb);

console.log("\nLoose inequality (!=):");
console.log(firstFavNumb != secondFavNumb);
